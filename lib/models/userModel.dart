class UserInfo {
  final String name;
  final String gender;
  final int age;
  final String occupation;
  final String skinType;
  final String acneType;
  final String openToNewProducts;
  final double acneSeverity;
  final String userImage1;
  final String userImage2;

  UserInfo({
    required this.name,
    required this.gender,
    required this.age,
    required this.occupation,
    required this.skinType,
    required this.acneType,
    required this.openToNewProducts,
    required this.acneSeverity,
    required this.userImage1,
    required this.userImage2,
  });
}
